    public function authenticate(Request $request)
    {
        $credentials = $request->validate([
            'username' => ['required'],
            'password' => ['required'],
        ]);
        //checking user admin or not. If he hasn't changed his password, he must change his password first
        if (Auth::attempt($credentials)) {
            $request->session()->regenerate();
            if (auth()->user()->is_admin == 1) {
                return redirect()->intended('/home');
            } else {
                if (auth()->user()->is_change_password == 0) {
                    return redirect()->intended('/user/' . auth()->user()->id . '/edit-password');
                }
                return redirect()->intended('/home');
            }
        }
        return back()->with('loginError', 'Login failed, try again');
    }
    public function logout(Request $request)
    {
        Auth::logout();

        $request->session()->invalidate();

        $request->session()->regenerateToken();

        return redirect('/');
    }

